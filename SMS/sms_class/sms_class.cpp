bool CSMS::SendATCmd(const char * szATCmd)
{
	s_buf.Init(szATCmd);
	if (WriteData(s_buf.pHead,s_buf.b_len,&s_buf.b_len)==true)
	{
		s_buf.Clear();
		return TRUE;
	}
	else
	{
		s_buf.Clear();
		return false;
	}
	
}

